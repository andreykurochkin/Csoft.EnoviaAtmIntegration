    /// <summary>
    /// Updates Enovia field pdSentToTDMS
    /// </summary>
    class T2EUpdateExportDate
    {
        private readonly TDMSApplication application;

        public T2EUpdateExportDate(TDMSApplication application)
        {
            this.application = application;
        }

        public void Execute()
        {
            var query = application.Queries["Q_T2E_CA_002"];
            if (query != null) Execute(query);
        }

        internal static void Execute(TDMSQuery query) => Execute(query.Objects);

        internal static void Execute(TDMSObjects tdmsObjects)
        {
            var client = new T2EUpdateExportDateHttpClient();
            var cache = CreateCache(tdmsObjects);
            cache.ToList().ForEach(c => Update(c, client));
        }

        internal static void Update(CacheTcaItem item, HttpClient httpClient)
        {
            var context = new DateUpdateContext(httpClient, item.Id, item.date);
            context.PutAsync();
        }

        private static IEnumerable<CacheTcaItem> CreateCache(TDMSObjects tdmsObjects)
        {
            var trash = tdmsObjects.ToList().
                Select(o => new
                {
                    id = new StringValueBehavior(
                        o.Attributes["A_ObjectGUID"]).GetValue(),
                    date = new DateTimeTdmsAttributeValueBehavior(
                        o.Attributes["A_Date1"]).GetValue()
                }).
                Where(t => (t.date != default));

            var garbage = trash.GroupBy(t => t.id).
                Select(g => new { id = g.Key, date = g.Max(c => c.date) });

            var result = garbage.Select(g =>
            {
                var id = g.id;
                var formattedDate = g.date.ToString("dd/MM/yyyy", System.Globalization.CultureInfo.InvariantCulture);
                return new CacheTcaItem(id, formattedDate);
            });

            return result;
        }
    }
